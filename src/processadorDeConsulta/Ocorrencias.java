package processadorDeConsulta;
/** Classe que armazena as Ocorrencias

 * @author Guilherme Alves

 * @version 0.1

 * @since Release 01

 */


public class Ocorrencias {
	String doc;
	int numOcorrencias,tamDoc;
	public String getDoc() {
		return doc;
	}
	public void setDoc(String doc) {
		this.doc = doc;
	}
	public int getNumOcorrencias() {
		return numOcorrencias;
	}
	public void setNumOcorrencias(int numOcorrencias) {
		this.numOcorrencias = numOcorrencias;
	}
	public int getTamDoc() {
		return tamDoc;
	}
	public void setTamDoc(int tamDoc) {
		this.tamDoc = tamDoc;
	}

}
